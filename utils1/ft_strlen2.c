#include "../cub3d.h"

int	ft_strlen2(char *str)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while (str[i] == '0' && str[i] != '\0')
		i++;
	while (str[i] != '\0')
	{
		j++;
		i++;
	}
	return (j);
}
