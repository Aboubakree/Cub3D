#include "../cub3d.h"

void	ft_putstr_fd(char *str)
{
	int	i;
	int	w;

	i = 0;
	w = 0;
	while (str[i])
	{
		w = write(2, &str[i], 1);
		(void) w;
		i++;
	}
}
